class Solution {
  vector<long long> table = {1, 4, 9, 121, 484, 12321, 14641, 44944, 1002001, 4008004, 1234321, 102030201, 104060401, 404090404, 123454321, 125686521, 10000200001, 40000800004, 10221412201, 12102420121, 12345654321, 1002003002001, 1004006004001, 4004009004004, 1022325232201, 1024348434201, 1212225222121, 1214428244121, 1234567654321, 100000020000001, 400000080000004, 100220141022001, 102012040210201, 102234363432201, 121000242000121, 121242363242121, 123212464212321, 123456787654321, 10002000300020001, 10004000600040001, 40004000900040004, 10022212521222001, 10024214841242001, 10203040504030201, 10205060806050201, 10223454745432201, 12102202520220121, 12104402820440121, 12124434743442121, 12323244744232321, 12345678987654321};
public:
  int superpalindromesInRange(string left, string right) {
    auto l = stoll(left);
    auto r = stoll(right);
    return lower_bound(table.begin(), table.end(), r + 1) - lower_bound(table.begin(), table.end(), l);
  }
};
